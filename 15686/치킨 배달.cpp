#include <iostream>
#include <vector>
#include <cmath>
using namespace std;

struct chikenInfo {
	int index;
	int row;
	int col;
};
int N, M;
vector<pair<int, int>> house;
vector<chikenInfo> chiken;
vector<chikenInfo> tempChiken;
int minValue = 999999999;
void DFS(int index, int depth) {
	if (depth == M) {
		/*
		for (int j = 0; j < tempChiken.size(); j++) {
			cout << " " << tempChiken[j].index;
		}
		cout << "\n";
		*/
		
		int sum = 0;
		int minDis;
		for (int i = 0; i < house.size(); i++) {
			minDis = 999999999;
			for (int j = 0; j < tempChiken.size(); j++) {
				minDis = min(minDis, abs(house[i].first - tempChiken[j].row) + abs(house[i].second - tempChiken[j].col));
			}
			sum += minDis;
		}
		//cout << sum <<"\n";
		minValue = min(minValue, sum);
		return;
	}
	for (int i = index; i < chiken.size(); i++) {
		tempChiken[depth].row = chiken[i].row;
		tempChiken[depth].col = chiken[i].col;
		//tempChiken[depth].index = chiken[i].index;
		DFS(i + 1, depth + 1);
	}
}
int main() {
	cin >> N >> M;
	int index = 0;
	int input;
	tempChiken.resize(M);
	for (int i = 1; i <= N; i++) {
		for (int j = 1; j <= N; j++) {
			cin >> input;
			if (input == 1) house.push_back({ i,j });
			if (input == 2) chiken.push_back({ index++, i, j });
		}
	}
	DFS(0,0);
	cout << minValue;
}
